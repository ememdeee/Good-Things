/**
 * FAQ Schema
 * insert the FAQ schema in WP_footer
 */
global $all_faqs;	// global variable to store the FAQs
$all_faqs = [];		// Reset the global variable

add_action('wp_footer', function() {
	global $all_faqs;	// global variable for the FAQs

	if (!empty($all_faqs)) {
		$faqSchema = [
			"@context" => "https://schema.org",
			"@type" => "FAQPage",
			"mainEntity" => array_map(function($faq) {
				return [
					"@type" => "Question",
					"name" => $faq['question'],
					"acceptedAnswer" => [
						"@type" => "Answer",
						"text" => $faq['answer']
					]
				];
			}, $all_faqs)
		];

		echo '<script type="application/ld+json">' . json_encode($faqSchema, JSON_UNESCAPED_SLASHES | JSON_UNESCAPED_UNICODE | JSON_PRETTY_PRINT) . '</script>';
	}
});


/**
 * User's Custom Script 
 * location: header, body and footer
 */
function cd_get_allowed_script_tags() {
	return array(
		'noscript' => array(), // Allow <noscript> tag
		'iframe' => array( // Allow <iframe> tag and its attributes
			'src' => array(),
			'height' => array(),
			'width' => array(),
			'style' => array(),
		),
		'script' => array( // Allow <script> tag and its attributes
			'src' => array(),
			'type' => array(),
			'async' => array(),
			'defer' => array(),
			'data-key' => array(),
		),
	);
}


/**
 * Override the favicon in both the front end and admin area.
 *
 * This function sets a custom favicon for the WordPress site by retrieving the favicon ID from the Theme Config Settings using ACF (Advanced Custom Fields). 
 * It outputs the necessary HTML tags to set the favicon for standard, high-resolution, and Apple touch icons.
 * 
 * The favicon ID is retrieved from the 'site_favicon' field in the ACF options page. If a valid favicon URL is found, it will be used to set the favicon 
 * for different device resolutions.
 * 
 * @return void Outputs HTML link tags for the favicon if a valid URL is found.
 */
function cd_custom_admin_favicon() {
	// Retrieve the favicon ID from the Theme Config Settings using ACF
	$favicon_id = get_field('site_favicon', 'option');
	
	// Check if a custom favicon is set
	if ($favicon_id) {
		// Get the URL of the favicon image in 'full' size
		$favicon_url = wp_get_attachment_image_url($favicon_id, 'full');
		
		// If a valid favicon URL is found, output the relevant HTML tags
		if ($favicon_url) {
			// Set the favicon for standard, high-resolution, and Apple touch icons
			echo '<link rel="icon" href="' . esc_url($favicon_url) . '" sizes="32x32" />';
			echo '<link rel="icon" href="' . esc_url($favicon_url) . '" sizes="192x192" />';
			echo '<link rel="apple-touch-icon" href="' . esc_url($favicon_url) . '" />';
		}
	}
}
// Apply the custom favicon to the WordPress admin area and login page
add_action('admin_head', 'cd_custom_admin_favicon');
add_action('login_head', 'cd_custom_admin_favicon');


/**
 * Modify the Gravity Forms submit button from an <input> to a <button> element.
 * This function preserves any custom button text defined in the form settings.
 * 
 * - Retrieves the submit button text using the Gravity Forms form object.
 * - Changes the default <input> to a <button> element for better flexibility and customization.
 * - Wraps the submit button text inside a <span class="link_text"> for styling purposes.
 * - Maintains the form-specific ID for the button.
 * 
 * @param string $button The original submit button HTML.
 * @param array $form The Gravity Forms form object, which contains form-specific data like the form ID and button text.
 * @return string Modified submit button HTML with a <button> element and wrapped text.
 */
// add_filter('gform_submit_button', 'cd_custom_gf_submit_button', 10, 2);
function cd_custom_gf_submit_button($button, $form) {
    // Get the submit button text from the form settings
    $submit_text = esc_html($form['button']['text']);

    // Create custom button HTML with the text wrapped in a <span class="link_text">
    return '<button class="gform_button button" type="submit" id="gform_submit_button_' . esc_attr($form['id']) . '"><span class="link-text">' . $submit_text . '</span></button>';
}


/**
 * Load more posts
 */
function cd_load_more_posts() {
	$paged = isset($_POST['page']) ? intval($_POST['page']) + 1 : 1;
	$category = isset($_POST['category']) ? $_POST['category'] : '';

	$args = array(
			'post_type' => 'post',
			'posts_per_page' => 12,
			'paged' => $paged,
			'post_status' => 'publish',
			'post__not_in' => get_option('sticky_posts'), // assume there's no sticky post in page 2 and so on
		);

	if ($category) {
			$args['category_name'] = $category;
	}

	$posts = new WP_Query($args);

	if ($posts->have_posts()) :
			while ($posts->have_posts()) : $posts->the_post();
					echo '<div class="col-md-6 col-lg-4">';
					get_template_part('loop-templates/content', get_post_format());
					echo '</div>';
			endwhile;
	endif;

	wp_die();
}
add_action('wp_ajax_load_more', 'cd_load_more_posts');
add_action('wp_ajax_nopriv_load_more', 'cd_load_more_posts');



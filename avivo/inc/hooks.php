function cd_add_opengraph_image() {
    // Check if an Open Graph image is already set
    global $post;
	if (isset($post) && $post) {
		$og_image = get_post_meta($post->ID, '_yoast_wpseo_opengraph-image', true);

		if (empty($og_image)) {
			// No Open Graph image is set, use the default image
			$default_og_image_id = get_field('default_og_image', 'option');
			if ($default_og_image_id) {
				$og_image_src = wp_get_attachment_image_src($default_og_image_id, 'full');
				if ($og_image_src) {
					echo '<meta property="og:image" content="' . esc_url($og_image_src[0]) . '" />';
				}
			}
		}
	}
}

/**
 * Outputs default Open Graph meta tags for the site if they are not already set by Yoast SEO.
 *
 * This function retrieves default Open Graph information from custom fields set in the WordPress options.
 * If these fields are set and Yoast SEO is not managing them, the function generates and outputs the appropriate meta tags for Open Graph.
 *
 * Open Graph tags included:
 * - `og:image`: The URL of the image to be used as the Open Graph image.
 * - `og:title`: The title to be used as the Open Graph title.
 * - `og:description`: The description to be used as the Open Graph description.
 *
 * This function is hooked into the `wp_head` action to ensure that these meta tags are included in the HTML `<head>` section of the page.
 *
 * Note: This function may create duplicate Open Graph tags if Yoast SEO is also setting them.
 * It is advisable to ensure that Yoast SEO and this function are not both setting the same Open Graph tags to avoid duplication.
 */
function cd_default_open_graph_info() {
	// Global default Open Graph settings
	$default_og_image_id = get_field('default_og_image', 'option');
	$default_og_title = get_field('default_og_title', 'option');
	$default_og_description = get_field('default_og_description', 'option');

	// Page/Post-specific Open Graph info
	global $post;
	$page_og_title = get_the_title($post->ID);
	$page_og_description = get_the_excerpt($post->ID);
	$page_og_image_id = get_post_thumbnail_id($post->ID);

	// Determine Open Graph values, prioritizing page-specific values
	$og_title = $page_og_title ? $page_og_title : $default_og_title;
	$og_description = $page_og_description ? $page_og_description : $default_og_description;

	if ($page_og_image_id) {
		$og_image = wp_get_attachment_image_src($page_og_image_id, 'full');
	} elseif ($default_og_image_id) {
		$og_image = wp_get_attachment_image_src($default_og_image_id, 'full');
	} else {
		$og_image = null;
	}

	// Get the current page URL
	$current_url = is_front_page() ? home_url() : get_permalink();

	// Output Open Graph meta tags
	if ($og_title) {
		echo '<meta property="og:title" content="' . esc_attr($og_title) . '" />';
	}

	if ($og_description) {
		echo '<meta property="og:description" content="' . esc_attr($og_description) . '" />';
	}

	if ($og_image && !empty($og_image[0])) {
		echo '<meta property="og:image" content="' . esc_url($og_image[0]) . '" />';
	}

	echo '<meta property="og:url" content="' . esc_url($current_url) . '" />';
}

